class Bebe
	def initialize
		puts "Bebé creado"
	end

	def llorar(llanto)
		puts "El bebé está llorando porque tiene #{llanto}."
	end

	def dormir(duerme)
		puts "El bebé está durmiendo #{duerme}"
	end

	def comer(comida)
		puts "El bebé se despertó y está comiendo #{comida}."
	end
end